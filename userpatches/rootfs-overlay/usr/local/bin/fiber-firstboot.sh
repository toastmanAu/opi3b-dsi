#!/bin/bash
# /usr/local/bin/fiber-firstboot.sh
# Runs once on first boot (triggered by fiber-firstboot.service)
# Sets up keys, Biscuit auth, NVMe detection, starts services.
#
# Runs on TTY1 so user sees it on the DSI display (if working)
# or can SSH in and watch.

set -e
ENVFILE=/etc/fiber-node.env
FIBER_DIR=/opt/fiber

clear
echo "╔══════════════════════════════════════════╗"
echo "║      FIBER KIOSK — FIRST BOOT SETUP      ║"
echo "╚══════════════════════════════════════════╝"
echo ""
echo "This wizard runs once and sets up your Fiber node."
echo ""

# ── 1. Check fnn binary ───────────────────────────────────────────
if [ -f "$FIBER_DIR/.build-needed" ]; then
    echo "► Building fnn from source (this takes ~20 minutes)..."
    echo "  Install Rust first..."
    curl --proto '=https' --tlsv1.2 -sSf https://sh.rustup.rs | sh -s -- -y --default-toolchain stable
    source "$HOME/.cargo/env"

    git clone --depth 1 --branch v0.7.0 \
        https://github.com/nervosnetwork/fiber.git /tmp/fiber-src
    cd /tmp/fiber-src
    cargo build --release
    cp target/release/fnn /opt/fiber/fnn
    chmod +x /opt/fiber/fnn
    rm -f "$FIBER_DIR/.build-needed"
    echo "  fnn built successfully."
fi

# ── 2. NVMe detection ─────────────────────────────────────────────
echo ""
NVME_DEV=$(ls /dev/nvme0n1 2>/dev/null || true)
if [ -n "$NVME_DEV" ]; then
    echo "► NVMe detected: $NVME_DEV"
    echo ""
    echo "  Use NVMe for Fiber channel database? (recommended for longevity)"
    echo "  [Y/n]: "
    read -r USE_NVME
    if [ "${USE_NVME:-y}" != "n" ] && [ "${USE_NVME:-y}" != "N" ]; then
        echo "  Formatting NVMe partition for Fiber data..."
        # Use existing partition or create one
        if ! blkid "${NVME_DEV}p1" &>/dev/null; then
            echo -e "g\nn\n1\n\n\nw" | fdisk "$NVME_DEV" >/dev/null 2>&1 || true
            sleep 1
            mkfs.ext4 -q -L fiber-data "${NVME_DEV}p1" 2>/dev/null || \
            mkfs.ext4 -q -L fiber-data "${NVME_DEV}" 2>/dev/null || true
        fi
        mkdir -p /opt/fiber/data
        NVME_PART="${NVME_DEV}p1"
        [ -b "$NVME_PART" ] || NVME_PART="$NVME_DEV"
        echo "LABEL=fiber-data  /opt/fiber/data  ext4  defaults,noatime  0  2" >> /etc/fstab
        mount /opt/fiber/data 2>/dev/null || true
        echo "  NVMe mounted at /opt/fiber/data ✓"
        USING_NVME=1
    else
        echo "  Using eMMC for Fiber data (ok for testing)"
        USING_NVME=0
    fi
else
    echo "► No NVMe detected — using eMMC for Fiber data"
    echo "  (Insert NVMe and re-run this wizard to migrate)"
    USING_NVME=0
fi
mkdir -p /opt/fiber/data/store
chown -R fiber:fiber /opt/fiber/data

# ── 3. CKB node URL ───────────────────────────────────────────────
echo ""
echo "► CKB Full Node RPC URL"
echo "  Default: http://127.0.0.1:8114/ (local node)"
echo "  Enter URL or press Enter for default: "
read -r CKB_RPC
CKB_RPC="${CKB_RPC:-http://127.0.0.1:8114/}"

# ── 4. Generate Biscuit keypair ───────────────────────────────────
echo ""
echo "► Generating Biscuit auth keypair..."
BISCUIT_OUT=$(python3 - << 'PYEOF'
from biscuit_auth import KeyPair, BiscuitBuilder, Algorithm
import os, base64

root = KeyPair()
pub_hex = 'ed25519/' + root.public_key.to_bytes().hex()
priv_hex = root.private_key.to_bytes().hex()

# Build full-access token (no expiry)
builder = BiscuitBuilder()
builder.add_code('read("node"); read("channels"); write("channels"); read("payments"); write("payments"); read("invoices"); write("invoices"); read("peers"); write("peers"); read("graph"); read("cch"); write("cch"); read("messages"); write("messages");')
token = builder.build(root.private_key)

print(f"PUB={pub_hex}")
print(f"PRIV={priv_hex}")
print(f"TOKEN={token.to_base64()}")
PYEOF
)

BISCUIT_PUB=$(echo "$BISCUIT_OUT" | grep '^PUB=' | cut -d= -f2-)
BISCUIT_PRIV=$(echo "$BISCUIT_OUT" | grep '^PRIV=' | cut -d= -f2-)
FIBER_TOKEN=$(echo "$BISCUIT_OUT" | grep '^TOKEN=' | cut -d= -f2-)
echo "  Keypair generated ✓"

# ── 5. Write Fiber config ─────────────────────────────────────────
echo ""
echo "► Writing Fiber config..."
cat > /opt/fiber/config/config.yml << FIBERCONF
fiber:
  listening_addr: "/ip4/0.0.0.0/tcp/8228"
  announced_addrs: []
  bootnode_addrs: []

ckb:
  rpc_url: "${CKB_RPC}"

rpc:
  listening_addr: "127.0.0.1:8227"
  biscuit_public_key: "${BISCUIT_PUB}"

store:
  path: "/opt/fiber/data/store"
FIBERCONF
chown fiber:fiber /opt/fiber/config/config.yml

# ── 6. Write env file (activates all services) ────────────────────
echo ""
echo "► Writing environment config..."
cat > "$ENVFILE" << ENVEOF
# Fiber node environment — generated by first-boot wizard
FIBER_SECRET_KEY_PASSWORD=$(cat /dev/urandom | tr -dc 'a-zA-Z0-9' | head -c 32)
FIBER_TOKEN=${FIBER_TOKEN}
FIBER_RPC_URL=http://127.0.0.1:8227
SIGNER_PORT=/dev/ttyACM0
BRIDGE_PORT=7777
BISCUIT_PUBLIC_KEY=${BISCUIT_PUB}
BISCUIT_PRIVATE_KEY=${BISCUIT_PRIV}
CKB_RPC_URL=${CKB_RPC}
USING_NVME=${USING_NVME}
ENVEOF
chmod 600 "$ENVFILE"
echo "  /etc/fiber-node.env written ✓"

# ── 7. Find touch device ──────────────────────────────────────────
echo ""
echo "► Detecting touch input device..."
sleep 2
TOUCH_DEV=$(ls /dev/input/event* 2>/dev/null | head -1)
if [ -n "$TOUCH_DEV" ]; then
    # Update kiosk config
    sed -i "s|touch_device.*|touch_device    = $TOUCH_DEV|" /etc/fiber-kiosk.conf
    echo "  Touch device: $TOUCH_DEV ✓"
else
    echo "  No touch device found yet — check /dev/input/ after reboot"
fi

# ── 8. Start services ─────────────────────────────────────────────
echo ""
echo "► Starting Fiber node..."
systemctl start fiber-node
sleep 5
systemctl start fiber-bridge
sleep 2
systemctl start fiber-kiosk

echo ""
echo "╔══════════════════════════════════════════╗"
echo "║           SETUP COMPLETE ✓               ║"
echo "╚══════════════════════════════════════════╝"
echo ""
echo "  Fiber node:   $(systemctl is-active fiber-node)"
echo "  Fiber bridge: $(systemctl is-active fiber-bridge)"
echo "  Kiosk UI:     $(systemctl is-active fiber-kiosk)"
echo ""
echo "  Biscuit public key:"
echo "  $BISCUIT_PUB"
echo ""
echo "  Keep /etc/fiber-node.env safe — it contains your node credentials."
echo ""
echo "  DSI display check:"
echo "  dmesg | grep -i 'raspberrypi\\|panel\\|dsi'"
echo ""

# Mark wizard complete
systemctl disable fiber-firstboot
